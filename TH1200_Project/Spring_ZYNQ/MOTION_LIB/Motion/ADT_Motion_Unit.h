/************************************************************************************************
 * 圆弧过渡处理专用结构体(枚举)列表:
 * 	  1. arc_blend_ini_struct
 * 	  2. ARC_BLEND_TYPE
 * 	  3. arc_blend_xyz
 * 	  4. arc_blend_inout_struct
 *
 * 圆弧过渡处理专用函数列表:
 * 	  1. int adt_set_arc_blend_parameter_initial_unit(arc_blend_ini_struct * InitPra);
 * 	  2. int adt_arc_blend_process_unit(arc_blend_inout_struct * PosPack);
 *
 * 备注：函数说明参看具体函数的头文件声明
************************************************************************************************/

typedef struct {
    double min_len; 			//过渡前后直线或圆弧的长度下限, 范围: 0.001-10, 单位:mm
    double min_R; 				//过渡圆弧的半径长度下限，范围: 0.001-10, 单位:mm。(若求出的过渡圆弧半径小于此值，则返回失败)
    double max_inter_angle; 	//过渡前直线段或圆弧间的夹角上限，范围: 135°-179.9°, 单位:degree(即：度)。(若夹角大于此值，不过渡)
    double min_inter_angle; 	//过渡前直线段或圆弧间的夹角下限，范围: 1°-90°, 单位:degree(即：度)。(若夹角小于此值，不过渡)
    double tolerance; 			//圆弧过渡精度值(过渡后最大误差)，范围: 0.001-10, 单位:mm
}arc_blend_ini_struct;

typedef enum {
    BLEND_LINE_LINE,	//直线到直线
    BLEND_LINE_ARC,		//直线到圆弧
    BLEND_ARC_LINE,		//圆弧到直线
    BLEND_ARC_ARC,		//圆弧到圆弧
} ARC_BLEND_TYPE;

typedef struct {
    double x;		//X 轴坐标
    double y;		//Y 轴坐标
    double z;		//Z 轴坐标
}arc_blend_xyz;

typedef struct {
	int type; 						//输入曲线类型, 有四种, 具体参照枚举 ARC_BLEND_TYPE 定义
    arc_blend_xyz seg1[3];			//输入第一条曲线(直线或圆弧)坐标数据
    arc_blend_xyz seg2[3]; 			//输入第二条曲线(直线或圆弧)坐标数据
    arc_blend_xyz blendok_seg1[3];	//过渡成功后，输出第一条曲线(直线或圆弧)坐标数据
    arc_blend_xyz blendok_seg2[3];	//过渡成功后，输出过渡圆弧坐标数据
    arc_blend_xyz blendok_seg3[3];	//过渡成功后，输出第三条曲线(直线或圆弧)坐标数据
    double BlendR;					//过渡成功后，输出过渡圆弧的半径
/*备注：对于结构体arc_blend_xyz定义的变量，例如：
   	第一段线段，若是直线, 则seg1[0]为直线起点，seg1[1]为直线终点
    			若是圆弧段，则seg1[0]为弧起点，seg1[1]为弧任意中间点(第二点)，seg1[2]为弧终点 */
}arc_blend_inout_struct;

/************************************************************************************************
 * 功能：对过渡圆弧处理时的参数阈值进行初始化
 * 		(基于脉冲当量编程)
 *
 * 参数：
 * 		arc_blend_ini_struct 	参数阈值(指针), 具体参照该结构体定义
 *
 * 返回值：参照错误码
 *
 * 备注：默认设置参数为：  min_len=1.0，min_R=1.0，max_inter_angle=175°， min_inter_angle=5°，tolerance=0.5
 ************************************************************************************************/
int adt_set_arc_blend_parameter_initial_unit(arc_blend_ini_struct * InitPra);

/************************************************************************************************
 * 功能：求相连曲线间的过渡圆弧
 * 		(基于脉冲当量编程)
 *
 * 参数：
 * 		arc_blend_inout_struct 	过渡前后数据点(指针), 具体参照该结构体定义
 *
 * 返回值：参照错误码
 *
 * 备注：若输入曲线存在圆弧段，不能为整圆，而且相连曲线须共面;
 * 		若输入曲线都是直线段，可以为空间任意相连线段;
 * 		过渡成功后, 发给运动库的插补数据一定要用输出后的位置量；
 * 		如果进来的数据都是X、Y轴平面上的点，这时Z轴位置也要给零，因为库底层默认处理的是空间数据
 ************************************************************************************************/
int adt_arc_blend_process_unit(arc_blend_inout_struct * PosPack);

/**************************************************************************************
*	圆弧拆分专用结构体arc_split_inout_struct
*	圆弧拆分专用函数接口arc_split(arc_split_inout_struct * inbp)
*	具体说明参见结构体和函数声明
***************************************************************************************/
typedef struct {
	double chord_erro;              //拆分允许的弓高误差(大于零)
	int split_type;                 //圆弧拆分的类型，分4种情况：0，line-arc-line；1，line-line-line；2，line-line；3，one line。
	arc_blend_xyz arc_pos[3];		//输入圆弧的坐标数据

	arc_blend_xyz split_seg1[3];	//拆分后，输出第一条曲线(直线或圆弧)坐标数据
	arc_blend_xyz split_seg2[3];	//拆分后，输出第二条曲线(直线或圆弧)坐标数据
	arc_blend_xyz split_seg3[3];	//拆分后，输出第三条曲线(直线或圆弧)坐标数据
									//拆分后根据split_type的值确定拆分结果，取用拆分后的数据
									//例如，拆分结果为line-line时，数据分别存储在split_seg1，split_seg2

									/*备注：对于结构体arc_blend_xyz定义的变量，例如：
									第一段线段，若是直线, 则seg1[0]为直线起点，seg1[1]为直线终点
									若是圆弧段，则seg1[0]为弧起点，seg1[1]为弧任意中间点(第二点)，seg1[2]为弧终点 */
}arc_split_inout_struct;

/******************************************************************************************
* 功能：对输入圆弧进行拆分
* 参数：arc_split_inout_struct 拆分前后的数据点（指针），具体说明参照结构体定义
*
* 返回值：参照错误码
*
* 备注： 若输入圆弧不能为整圆;
* 	       拆分成功后, 发给运动库的插补数据一定要用输出后的位置量；
*	       拆分后根据split_type的值确定拆分结果，取用拆分后的数据，
* 	        如果进来的数据都是X、Y轴平面上的点，这时Z轴位置也要给零，因为库底层默认处理的是空间数据
*******************************************************************************************/
int adt_arc_split(arc_split_inout_struct * inbp);






