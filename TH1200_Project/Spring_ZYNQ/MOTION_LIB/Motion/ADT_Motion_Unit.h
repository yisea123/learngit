#define		ERR_ARCBLEND_EXECUTE_FAIL	16			// 求解过渡圆过程中无解(圆弧过渡处理)
#define		ERR_ARCBLEND_INCOP_ARCARC	17			// 存在两条圆弧段时输入曲线不共面(圆弧过渡处理)


